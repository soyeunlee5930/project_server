package project.project.requestParam;

public class ProductOptionDetails {
    private Integer productOptionsId;
    private Integer productId;
    private String productName;
    private Integer colorId;
    private String color;
    private Integer sizeId;
    private Integer size;
    private Integer quantity;

    public Integer getProductOptionsId() {
        return productOptionsId;
    }

    public void setProductOptionsId(Integer productOptionsId) {
        this.productOptionsId = productOptionsId;
    }

    public Integer getProductId() {
        return productId;
    }

    public void setProductId(Integer productId) {
        this.productId = productId;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public Integer getColorId() {
        return colorId;
    }

    public void setColorId(Integer colorId) {
        this.colorId = colorId;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public Integer getSizeId() {
        return sizeId;
    }

    public void setSizeId(Integer sizeId) {
        this.sizeId = sizeId;
    }

    public Integer getSize() {
        return size;
    }

    public void setSize(Integer size) {
        this.size = size;
    }

    public Integer getQuantity() {
        return quantity;
    }

    public void setQuantity(Integer quantity) {
        this.quantity = quantity;
    }
}
